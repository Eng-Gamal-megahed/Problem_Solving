int find_gcd(int a, int b)
{
    while (b != 0)
    {
        int temp = b;
        b = a % b;
        a = temp;
    }
    return a;
}

void solve() {
    int t;
    cin >> t;
    while (t--)
    {
        int y;
        int x;
        cin >> x;
        int maxx = find_gcd(x, 1) + 1;
        y = 1;
        for (int i = 2; i < x; i++)
        {
            if (find_gcd(x, i) + i >= maxx)
            {
                maxx = find_gcd(x, i) + i;
                y = i;
            }
        }
        cout << y << "\n";
    }
}
