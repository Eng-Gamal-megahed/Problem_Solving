void solve() {
    string s;
    cin >> s;
    vector<string>ans;
    sort(all(s));
    do {
        ans.emplace_back(s);
    } while (next_permutation(all(s)));
    cout << ans.size() << nl;
    for (ll i = 0; i < ans.size(); ++i) {
        cout << ans[i] << nl;
    }
}
