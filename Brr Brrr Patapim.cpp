void solve() {
    ll n;
    cin >> n;
    vector<vector<ll>>a(n, vector<ll>(n));
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            cin >> a[i][j];
        }
    }
    vector<ll>ans(2 * n);
    vector<bool>vis(2 * n + 1);
    for (int i = 0; i < n; ++i) {
        for (int j = i; j < n; ++j) {
            ans[i + j + 1] = a[i][j];
            vis[a[i][j]] = true;
        }
    }
    for (int i = 1; i <= 2 * n; ++i) {
        if(!vis[i])
        {
            ans[0] = i;
            break;
        }
    }
    for (int i = 0; i < 2 * n; ++i) {
        cout << ans[i] << sp;
    }
    cout << nl;
}
