int n , m ;
ll danger = 1;
vector<int> adj[50 + 1];
vector<bool> vis(50 + 1 , false);

void dfs(int v)
{
    vis[v] = true;
    for (int u: adj[v]) {
        if(!vis[u])
        {
            danger *= 2;
            dfs(u);
        }
    }
}
void solve() {
    cin >> n >> m;
    for (ll i = 0; i < m; ++i) {
        int a , b;
        cin >> a >> b;
        adj[a].push_back(b);
        adj[b].push_back(a);
    }
    for (ll i = 1; i <= n; ++i) {
        if(!vis[i])
        {
            dfs(i);
        }
    }
    cout << danger << nl;
}
