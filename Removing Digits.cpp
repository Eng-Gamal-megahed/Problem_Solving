const int N = 1e6 + 5, M = 1e3, LOG = 20, inf = 0x3f3f3f3f;
ll infLL = 0x3f3f3f3f3f3f3f3f;

ll n , dp[N];

void solve() {
    cin >> n;
    memset(dp , infLL , sizeof dp);
    dp[n] = 0;
    for (ll i = n ; i > 0; --i) {
        if(dp[i] == infLL)
            continue;
        ll cur = i;
        while (cur)
        {
            dp[i - (cur % 10)] = min(dp[i - (cur % 10)] , dp[i] + 1);
            cur /= 10;
        }
    }
    cout << dp[0] << nl;
}
