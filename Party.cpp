const int N = 2e3 + 1;
int n , ans = 0;
vector<vector<ll>> adj(N);
vector<bool>root(N , false) , vis(N , false);

int dfs(int v , int cnt)
{
    vis[v] = true;
    cnt++;
    int c = cnt;
    for(int u : adj[v])
    {
        if(!vis[u])
        cnt = max(cnt , dfs(u , c));
    }
    return cnt;
}
void solve() {
    cin >> n ;
    for (ll i = 1; i <= n; ++i) {
        ll a ;
        cin >> a;
        if(a == -1)
        {
            root[i] = true;
            continue;
        }
        adj[a].push_back(i);
    }
    for (ll i = 1; i <= n; ++i) {
        if(root[i])
        {
            ans = max(dfs(i , 0) , ans);
        }
    }
    cout << ans << nl;
}
