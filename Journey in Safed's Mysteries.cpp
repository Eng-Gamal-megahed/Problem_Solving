void solve() {
    ll n , h , b;
    cin >> n >> h >> b;
    vector<pair<ll , int>>sharks(n);
    for (ll i = 0; i < n; ++i) {
        cin >> sharks[i].first >> sharks[i].second;
    }
    ll ans = 0;
    for (ll i = 0; i < n; ++i) {
        if(sharks[i].second)
            b--;
        if(b == -1)
            break;
        h -= sharks[i].first;
        if(h + b >= 1)
            ans++;
        else
            break;
    }
    cout << ans << nl;
}
