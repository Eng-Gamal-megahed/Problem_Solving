ll X[N] , H[N] , n ;

int calc(int i, ll last) {
    if (i >= n)
        return 0;
    if (X[i] - H[i] > last)
        return 1 + calc(i + 1, X[i]);
    if (X[i] + H[i] < X[i + 1])
        return 1 + calc(i + 1, X[i] + H[i]);
    return calc(i + 1, X[i]);
}

