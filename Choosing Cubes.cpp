void solve() {
    ll n , f , k;
    cin >> n >> f >> k;
    vector<ll>v(n);
    for (ll i = 0; i < n; ++i) {
        cin >> v[i];
    }
    if(k == n)
    {
        yes;
        return;
    }
    ll cur = v[f - 1];
    sort(allr(v));
    if(v[k - 1] == cur && v[k] == cur)
    {
        cout << "MAYBE" << nl;
    }
    else if(v[k - 1] > cur)
    {
        no;
    }
    else
    {
        yes;
    }
}
