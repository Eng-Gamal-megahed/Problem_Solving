void solve() {
    string s;
    cin >> s;
    int zeros = 0 , ones = 0 , ans = s.size();
    for (ll i = 0; i < s.size(); ++i) {
        (s[i] == '1'? ones++ : zeros++);
    }
    for (ll i = 0; i < s.size(); ++i) {
        if(s[i] == '1')
        {
            if(zeros--)
                ans--;
            else
            {
                cout << ans << nl;
                return;
            }
        } else
        {
            if(ones--)
                ans--;
            else
            {
                cout << ans << nl;
                return;
            }
        }
    }
    cout << ans << nl;
}
