void solve() {
    ll n;
    cin >> n;
    vector<ll>ans = {n};
    for (ll i = 0; i < 60; ++i) {
        if(((1ll << i) & n) && (1ll << i) != n)
        {
            ans.push_back(n - (1ll << i));
        }
    }
    cout << ans.size() << nl;
    reverse(all(ans));
    for (ll i = 0; i < ans.size(); ++i) {
        cout << ans[i] << ' ';
    }
    cout << nl;
}
