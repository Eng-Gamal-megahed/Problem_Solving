void solve() {
    ll a , b;
    cin >> a >> b;
    ll a_bits = floor(log2(a)) + 1 , b_bits = floor(log2(b)) + 1;
    ll ans = 0;
    for (ll bits = a_bits; bits <= b_bits ; ++bits) {
        for (ll i = 0; i < bits - 1; ++i) {
            ll year = ((1ll << bits) - 1) - (1ll << i);
            if(year >= a && year <= b)
            {
                ans++;
            }
        }
    }
    cout << ans << nl;
}
