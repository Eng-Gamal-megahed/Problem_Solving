const ll MOD = 1e9 + 7;

ll add(ll a, ll b)
{
    return ((a % MOD) + (b % MOD)) % MOD;
}

ll sub(ll a, ll b)
{
    return ((a % MOD) - (b % MOD) + MOD) % MOD;
}

ll mul(ll a, ll b)
{
    return ((a % MOD) * (b % MOD)) % MOD;
}

ll dp[N] , k , a , b;

void calc()
{
    dp[0] = 1;
    for (int i = 1; i < N; ++i) {
       dp[i] = dp[i - 1];
       if(i >= k)
           dp[i] = add(dp[i - k], dp[i]);
    }

    for (int i = 1; i < N; ++i) {
        dp[i] = add(dp[i] , dp[i - 1]);
    }
}

void solve() {
    cin >> a >> b;
    cout << sub(dp[b] , dp[a - 1]) << nl;
}
