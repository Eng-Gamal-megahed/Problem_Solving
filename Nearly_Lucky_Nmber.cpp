{
    string s;
    cin >> s;
    int counter = 0;
    for (char c : s)
    {
        if (c == '4' || c == '7')
        {
            counter++;
        }
    }
    string s_counter = to_string(counter);
    for (char c : s_counter)
    {
        if (c != '4' && c != '7')
        {
            cout << "NO\n";
            return;
        }
    }
    cout << "YES\n";
}

