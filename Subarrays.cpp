void solve() {
    ll n , k;
    cin >> n >> k;
    vector<ll>v(n) , pre(n + 1);
    for (ll i = 0; i < n; ++i) {
        cin >> v[i];
    }
    for (ll i = 1; i <= n; ++i) {
        pre[i] = pre[i - 1] + v[i - 1];
    }
    unordered_map<ll , ll>freq;
    ll ans = 0;
    freq[0]++;
    for (ll i = 1; i <= n; ++i) {
        ans += freq[pre[i] % k];
        freq[pre[i] % k]++;
    }
    cout << ans << nl;
}
