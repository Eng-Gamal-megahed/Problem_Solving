void solve() {
    ll n  , m;
    cin >> n >> m;
    string s;
    cin >> s;
    map<char , ll>freq;
    for (ll i = 0; i < n; ++i) {
        freq[s[i]]++;
    }
    ll ans = 0;
    for (char c = 'A'; c <= 'G'; c++) {
        if(freq[c] < m)
        {
            ans += m - freq[c];
        }
    }
    cout << ans << nl;
}
