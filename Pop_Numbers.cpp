void solve() {
    ll n , q;
    cin >> n >> q;
    vector<ll>v(n) , pre(n + 1);
    for (ll i = 0; i < n; ++i) {
        cin >> v[i];
        ll cnt = 0;
        while (v[i] != 1)
        {
            if(v[i] % 2)
            {
                v[i]--;
                cnt++;
            }
            else
            {
                v[i] /= 2;
                cnt++;
            }
        }
        v[i] = cnt;
        pre[i + 1] = pre[i] + v[i];
    }
    for (ll i = 0; i < q; ++i) {
        ll l , r;
        cin >> l >> r;
        cout << pre[r] - pre[l - 1] << nl;
    }
}
