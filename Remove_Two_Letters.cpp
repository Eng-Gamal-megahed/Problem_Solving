string pattern(string &s) {
    int n = s.size();
    for (int len = 1; len <= n; ++len) {
        if (n % len == 0) {
            string pattern = s.substr(0, len);
            std::string repeated = "";
            for (int i = 0; i < n / len; ++i) {
                repeated += pattern;
            }
            if (repeated == s) {
                return pattern;
            }
        }
    }
    return s;
}

