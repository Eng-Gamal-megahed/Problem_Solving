void solve() {
    ll n;
    cin >> n;
    vector<ll>a(n);
    ll sum = 0;
    for (ll i = 0; i < n; ++i) {
        cin >> a[i];
        sum += a[i];
    }
    ll ans = sum;
    for (ll i = 0; i < n - 1; ++i) {
        ans = max(ans , abs(a.front() - a.back()));
        vector<ll>n_a(a.size() - 1);
        for (ll j = 1; j < a.size(); ++j) {
            n_a[j - 1] = a[j] - a[j - 1];
        }
        a = n_a;
    }
    cout << ans << nl;
}
